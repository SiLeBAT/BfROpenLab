package de.bund.bfr.knime.gis.views.canvas.util;

import java.util.LinkedHashMap;
import java.util.Map;

public class NodePropertySchema extends PropertySchema {

	private String id;

	private String latitude;
	private String longitude;

	public NodePropertySchema() {
		this(new LinkedHashMap<>(0), null);
	}

	public NodePropertySchema(Map<String, Class<?>> map, String id) {
		super(map);
		this.id = id;
		latitude = null;
		longitude = null;
	}

	public String getId() {
		return id;
	}

	public String getLatitude() {
		return latitude;
	}

	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	public String getLongitude() {
		return longitude;
	}

	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	@Override
	public Type getType() {
		return Type.NODE;
	}
}
